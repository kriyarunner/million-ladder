export type Lang = "da" | "en";

export const LOCALES: Lang[] = ["da", "en"];
export const DEFAULT_LANG: Lang = "da";

/** Returnér locale ud fra en pathname (fx "/en/blog" -> "en"). */
export function localeFromPath(pathname: string): Lang {
  return pathname === "/en" || pathname.startsWith("/en/") ? "en" : "da";
}

/** Læg locale-præfiks på en intern sti. da = ingen præfiks, en = "/en". */
export function withLocale(lang: Lang, path: string): string {
  const clean = path.startsWith("/") ? path : `/${path}`;
  if (lang === "da") return clean;
  if (clean === "/") return "/en";
  if (clean.startsWith("/#")) return `/en${clean.slice(1)}`;
  return `/en${clean}`;
}

/** Fjern locale-præfiks fra en sti (til at finde "samme side" på det andet sprog). */
export function stripLocale(pathname: string): string {
  if (pathname === "/en") return "/";
  if (pathname.startsWith("/en/")) return pathname.slice(3);
  return pathname;
}

